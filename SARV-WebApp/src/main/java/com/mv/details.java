package com.mv;

public class details {
	public String antecedent = "";
	public String consequent = "";
	public String supCount = "";
	public String confValue = "";
	public String liftValue = "";
	public String levValue = "";
	public String convValue = "";

	public details(String antecedent, String consequent, String supCount, String confValue, String liftValue, String levValue, String convValue) {
		this.antecedent = antecedent;
		this.consequent = consequent;
		this.supCount = supCount;
		this.confValue = confValue;
		this.liftValue = liftValue;
		this.levValue = levValue;
		this.convValue = convValue;
	}
}
